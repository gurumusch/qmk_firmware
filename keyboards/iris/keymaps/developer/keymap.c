bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        set_single_persistent_default_layer(_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case ADJUST:
      if (record->event.pressed) {
        layer_on(_ADJUST);
      } else {
        layer_off(_ADJUST);
      }
      return false;
      break;
    case SML_H1:
      if (record->event.pressed) {
        SEND_STRING(":-)");
      }
      return false;
      break;
    case SML_H2:
      if (record->event.pressed) {
        SEND_STRING(";-)");
      }
      return false;
      break;
    case SML_S1:
      if (record->event.pressed) {
        SEND_STRING(":-(");
      }
      return false;
      break;
    case KC_ML1:
      if (record->event.pressed) {
        SEND_STRING("schaefer.a@eplan.de");
      }
      return false;
      break;
    case KC_ML2:
      if (record->event.pressed) {
        SEND_STRING("gurumusch@gmail.com");
      }
      return false;
      break;
    case CTL_1:
      if (record->event.pressed) {
        SEND_STRING(SS_LCTRL("1"));
      }
      return false;
      break;
    case CTL_2:
      if (record->event.pressed) {
        SEND_STRING(SS_LCTRL("2"));
      }
      return false;
      break;
    case CTL_3:
      if (record->event.pressed) {
        SEND_STRING(SS_LCTRL("3"));
      }
      return false;
      break;
    case CTL_4:
      if (record->event.pressed) {
        SEND_STRING(SS_LCTRL("4"));
      }
      return false;
      break;
    case CTL_5:
      if (record->event.pressed) {
        SEND_STRING(SS_LCTRL("5"));
      }
      return false;
      break;
  }
  return true;
}
